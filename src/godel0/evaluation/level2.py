"""Level 2 evaluator: frontier accuracy on newly generated tasks."""

from __future__ import annotations

from collections import defaultdict
from typing import Dict, List

from ..schemas.evaluation import EvaluationOutcome, Level2Result


class Level2Evaluator:
    """Evaluates the solver on the current proposer's newly generated tasks.

    Level 2 runs the Current Solver on T_child (tasks generated by the
    Current Proposer). The accuracy p = solved / total is used in scoring.

    P1-4: when ``solver_rollouts > 1``, ``outcomes`` may contain multiple
    entries per task_id. Accuracy is the mean success rate over all rollouts;
    solved/failed task lists are unique task ids (majority vote across
    rollouts; ties count as failed).
    """

    def compute_accuracy(
        self,
        node_id: str,
        task_batch_id: str,
        outcomes: List[EvaluationOutcome],
    ) -> Level2Result:
        """Compute frontier accuracy from solver outcomes.

        Args:
            node_id: The node being evaluated.
            task_batch_id: The batch of tasks being solved.
            outcomes: Solver outcomes on each task (possibly multiple rollouts).

        Returns:
            Level2Result with accuracy and solved/failed lists.
        """
        if not outcomes:
            return Level2Result(
                node_id=node_id,
                task_batch_id=task_batch_id,
                evaluated_task_ids=[],
                solved_task_ids=[],
                failed_task_ids=[],
                accuracy=0.0,
                outcomes=[],
            )

        by_task: Dict[str, List[bool]] = defaultdict(list)
        for outcome in outcomes:
            by_task[outcome.task_id].append(bool(outcome.resolved))

        evaluated_task_ids = list(by_task.keys())
        solved_task_ids: List[str] = []
        failed_task_ids: List[str] = []
        for task_id, flags in by_task.items():
            # Majority resolved => solved; otherwise failed (ties fail).
            if sum(1 for flag in flags if flag) > len(flags) / 2:
                solved_task_ids.append(task_id)
            else:
                failed_task_ids.append(task_id)

        # Mean over rollouts (not unique tasks) so N rollouts feed scoring
        # and stochasticity with the same outcome list.
        accuracy = sum(1 for o in outcomes if o.resolved) / len(outcomes)

        return Level2Result(
            node_id=node_id,
            task_batch_id=task_batch_id,
            evaluated_task_ids=evaluated_task_ids,
            solved_task_ids=solved_task_ids,
            failed_task_ids=failed_task_ids,
            accuracy=accuracy,
            outcomes=outcomes,
        )
