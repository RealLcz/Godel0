"""Level 2 evaluator: frontier accuracy on newly generated tasks."""

from __future__ import annotations

from typing import List

from ..schemas.evaluation import EvaluationOutcome, Level2Result


class Level2Evaluator:
    """Evaluates the solver on the current proposer's newly generated tasks.

    Level 2 runs the Current Solver on T_child (tasks generated by the
    Current Proposer). The accuracy p = solved / total is used in scoring.
    """

    def compute_accuracy(
        self,
        node_id: str,
        task_batch_id: str,
        outcomes: List[EvaluationOutcome],
    ) -> Level2Result:
        """Compute frontier accuracy from solver outcomes.

        Args:
            node_id: The node being evaluated.
            task_batch_id: The batch of tasks being solved.
            outcomes: Solver outcomes on each task.

        Returns:
            Level2Result with accuracy and solved/failed lists.
        """
        if not outcomes:
            return Level2Result(
                node_id=node_id,
                task_batch_id=task_batch_id,
                evaluated_task_ids=[],
                solved_task_ids=[],
                failed_task_ids=[],
                accuracy=0.0,
                outcomes=[],
            )

        solved = [o.task_id for o in outcomes if o.resolved]
        failed = [o.task_id for o in outcomes if not o.resolved]
        accuracy = len(solved) / len(outcomes)

        return Level2Result(
            node_id=node_id,
            task_batch_id=task_batch_id,
            evaluated_task_ids=[o.task_id for o in outcomes],
            solved_task_ids=solved,
            failed_task_ids=failed,
            accuracy=accuracy,
            outcomes=outcomes,
        )
